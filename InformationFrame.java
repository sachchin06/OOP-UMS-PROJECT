    void display(String userName){
        
        try {
            
           
            Class.forName("com.mysql.jdbc.Driver");
            conn=DriverManager.getConnection("jdbc:mysql://localhost/ums","root","Murukan1998");
            
            
             pst=conn.prepareStatement(" select name,age,gender,telephone from registration where username=?");
             pst.setString(1,userName);
             ResultSet rs=pst.executeQuery();
             
             while(rs.next()){
                 
                 txt_name.setText(rs.getString(1));
                 txt_age.setText(rs.getString(2));
                 txt_gender.setText(rs.getString(3));
                 txt_teleNo.setText(rs.getString(4 ));
                 
                 
             }
             
   
        } 
        catch (ClassNotFoundException ex) {
            Logger.getLogger(InformationFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        catch (SQLException ex) {
            Logger.getLogger(InformationFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
           
        
        
    }
    
